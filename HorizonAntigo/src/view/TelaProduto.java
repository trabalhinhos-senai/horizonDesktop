    private void btnSalvarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarProdutoActionPerformed

        String sv = cxtPrecoVendaProd.getText();
        String valorSemFormatacao = sv.replace("R$", "").replace(" ", "").replace(".", "").replace(",", ".");
        BigDecimal valor = new BigDecimal(valorSemFormatacao);
        NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
        String valorFormatado = nf.format(valor);
        cxtPrecoVendaProd.setText(valorFormatado);
        //System.out.println(valorSemFormatacao);

        String sv1 = cxtPrecoCustoProd.getText();
        String valorSemFormatacao1 = sv1.replace("R$", "").replace(" ", "").replace(".", "").replace(",", ".");
        BigDecimal valor1 = new BigDecimal(valorSemFormatacao1);
        NumberFormat nf1 = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
        String valorFormatado1 = nf1.format(valor1);
        cxtPrecoCustoProd.setText(valorFormatado1);
        //System.out.println(valorSemFormatacao);

        try {
            //verifica o nome se é diferente de vazio
            if (cxtNomeProduto.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(null, "O nome não pode estar em branco!");

            } else if (cxtPrecoVendaProd.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O preço de venda não pode estar em branco!");

            } else if (tipoSituacao != 1 && tipoSituacao != 2) {
                JOptionPane.showMessageDialog(null, "A situação não pode estar em branco!");

            } else if (cxtNomeProduto.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(null, "O fornecedor não pode estar em branco!");
            } else {
                produto.setNomeProd(cxtNomeProduto.getText());
                produto.setCodBarrasProd(Integer.parseInt(cxtCodBarrasProd.getText()));
                produto.setCodRefProd(Integer.parseInt(cxtCodReferenciaProd.getText()));
                produto.setPrecoCustoProd(BigDecimal.valueOf(Double.parseDouble(cxtPrecoCustoProd.getText().replace("R$", "").replace(" ", "").replace(".", "").replace(",", "."))));
                produto.setPrecoVendaProd(BigDecimal.valueOf(Double.parseDouble(cxtPrecoVendaProd.getText().replace("R$", "").replace(" ", "").replace(".", "").replace(",", "."))));
                produto.setCategoriaProd(cmbCategoriaProd.getSelectedItem().toString());
                produto.setUnidMedidaProd(cmbUnidadeProd.getSelectedItem().toString());
                produto.setMargemLucroProd(Double.parseDouble(cxtMargemLucroProd.getText()));
                produto.setObservacaoProd(txtAreaObservacao.getSelectedText());
                produto.setNomeFornecedor(txtNomeForncedor.getText());
                produto.setDataAlteracaoProd(Date.from(Instant.now()));
                produto.setDataCadastroProd(Date.from(Instant.now()));

                switch (tipoSituacao) {
                    //1 para sexo Masculino e 2 para sexo feminino
                    case 1:
                        produto.setSituacaoProd(1);
                        break;
                    case 2:
                        produto.setSituacaoProd(2);
                        break;
                    default:
                        produto.setSituacaoProd(0);
                        break;
                }
                pRep.salvarAdd(produto);
                JOptionPane.showMessageDialog(null, "Cadastro salvo com sucesso!");
                AddProdutoCadastro.setVisible(false);
                refreshTable();
                refreshTableFornecedor();
                limpar();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Dados incompletos ou incorretos, \n'*' Verifique as informações necessárias para continuar!");
        }

    }//GEN-LAST:event_btnSalvarProdutoActionPerformed

