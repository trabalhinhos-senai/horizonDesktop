    private void btnExcluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirProdutoActionPerformed
        try {
            ProdutoRep pRep = new ProdutoRep();
            List<TbProdutos> produto = pRep.listar();
            if (produto.get(tblProduto.getSelectedRow()) != null) {
                TbProdutos produtoSelecionado = produto.get(tblProduto.getSelectedRow());
                pRep.excluir(produtoSelecionado);
                JOptionPane.showMessageDialog(null, "Produto excluído com sucesso!");
                refreshTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não há nada para excluir.");
        }


    }//GEN-LAST:event_btnExcluirProdutoActionPerformed

